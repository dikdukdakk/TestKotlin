fun main(args: Array<String>){
    //What is the diffrence bts var and val
    // var : mutable .. you can change var
    // val : immutable .. you cant change val

    var mutableVar : String = "Nuttapon"
    mutableVar = "1234"
    println(mutableVar)

    //val = final (java) or let (switf)
    val mutableVal : String = "Nuttapon"
    println(mutableVal)
}